# 정렬 알고리즘 - 데이터를 특정 기준(오름차순/내림차순)에 따라 나열하는 기법

# 1. 정렬 알고리즘을 배우는 이유
# 1) 코딩테스트에서 직접 구현 문제 자주 출제
# 2) 각 알고리즘의 시간 복잡도 이해 → 상황에 맞는 선택
# 3) 알고리즘적 사고력 향상

# 2. 버블 정렬 (Bubble Sort) - 인접한 두 값을 비교해서 교환하는 방식

# [5, 3, 8, 1, 4] 를 정렬

# 1라운드
# 5, 3 비교 → 5 > 3 → 교환 [3, 5, 8, 1, 4]
# 5, 8 비교 → 5 < 8 → 유지 [3, 5, 8, 1, 4]
# 8, 1 비교 → 8 > 1 → 교환 [3, 5, 1, 8, 4]
# 8, 4 비교 → 8 > 4 → 교환 [3, 5, 1, 4, 8] ← 8 자리 확정

# 2라운드
# 3, 5 비교 → 유지
# 5, 1 비교 → 교환 [3, 1, 5, 4, 8]
# 5, 4 비교 → 교환 [3, 1, 4, 5, 8] ← 5 자리 확정

# ... 반복

def bubble_sort(nums):
    n = len(nums)
    for i in range(n):
        # i 번 라운드가 끝나면 뒤에서 i개는 이미 정렬됨
        # 그래서 n - i - 1 까지만 비교
        for j in range(0, n - i - 1):
            if nums[j] > nums[j + 1]:
                # 파이썬 스왑 — temp 변수 없이 가능
                nums[j], nums[j + 1] = nums[j + 1], nums[j]
    return nums

print(bubble_sort([5, 3, 8, 1, 4]))   # [1, 3, 4, 5, 8]

# -----------------------------
# 시간복잡도: O(n²)
# 공간복잡도: O(1)
# 특징: 구현 쉽지만 느림
#------------------------------


# 3. 선택 정렬 (Selection Sort) - 가장 작은 값을 찾아서 앞으로 가져오는 방식

# [5, 3, 8, 1, 4] 를 정렬

# 1라운드 — 전체에서 최솟값 찾기
# 최솟값 = 1 (인덱스 3)
# 맨 앞(인덱스 0) 과 교환
# [1, 3, 8, 5, 4] ← 1 자리 확정

# 2라운드 — 나머지에서 최솟값 찾기
# [3, 8, 5, 4] 에서 최솟값 = 3 (이미 맨 앞)
# 교환 없음
# [1, 3, 8, 5, 4] ← 3 자리 확정

# 3라운드
# [8, 5, 4] 에서 최솟값 = 4 (인덱스 4)
# 인덱스 2 와 교환
# [1, 3, 4, 5, 8] ← 4 자리 확정

# ... 반복

def selection_sort(nums):
    n = len(nums)
    for i in range(n):
        min_idx = i    # 현재 위치를 최솟값 인덱스로 가정

        # i+1 부터 끝까지 탐색하면서 더 작은 값 찾기
        for j in range(i + 1, n):
            if nums[j] < nums[min_idx]:
                min_idx = j    # 더 작은 값 발견 → 인덱스 업데이트

        # 최솟값을 현재 위치로 교환
        nums[i], nums[min_idx] = nums[min_idx], nums[i]
    return nums

print(selection_sort([5, 3, 8, 1, 4]))  # [1, 3, 4, 5, 8]

# -----------------------------
# 시간복잡도: O(n²)
# 공간복잡도: O(1)
# 특징: 교환 횟수가 적음
#------------------------------


# 4. 삽입 정렬 (Insertion Sort) - 카드 게임처럼 하나씩 뽑아서 올바른 위치에 삽입 하는 방식

# [5, 3, 8, 1, 4] 를 정렬

# i=1, key=3
# [5, _, 8, 1, 4]
# 5 > 3 → 5를 한 칸 뒤로
# [_, 5, 8, 1, 4]
# key(3) 를 빈 자리에 삽입
# [3, 5, 8, 1, 4]

# i=2, key=8
# [3, 5, _, 1, 4]
# 5 < 8 → 멈춤
# key(8) 를 빈 자리에 삽입
# [3, 5, 8, 1, 4]

# i=3, key=1
# [3, 5, 8, _, 4]
# 8 > 1 → 8 한 칸 뒤로 [3, 5, _, 8, 4]
# 5 > 1 → 5 한 칸 뒤로 [3, _, 5, 8, 4]
# 3 > 1 → 3 한 칸 뒤로 [_, 3, 5, 8, 4]
# key(1) 를 빈 자리에 삽입
# [1, 3, 5, 8, 4]

# ... 반복

def insertion_sort(nums):
    for i in range(1, len(nums)):
        key = nums[i]    # 현재 삽입할 값
        j = i - 1        # 왼쪽 끝부터 비교 시작

        # key 보다 큰 값들을 한 칸씩 뒤로 밀기
        while j >= 0 and nums[j] > key:
            nums[j + 1] = nums[j]   # 한 칸 뒤로
            j -= 1

        nums[j + 1] = key   # 올바른 위치에 삽입
    return nums

print(insertion_sort([5, 3, 8, 1, 4]))   # [1, 3, 4, 5, 8]

# -----------------------------
# 시간복잡도: O(n²) / 거의 정렬된 경우 O(n)
# 공간복잡도: O(1)
# 특징: 거의 정렬된 데이터에서 빠름
#------------------------------


# 5. 퀵 정렬 (Quick Sort) - 기준값(pivot) 을 정하고 작은 것은 왼쪽, 큰 것은 오른쪽 으로 나누는 방식

# [5, 3, 8, 1, 4] 를 정렬

# pivot = 3 (중간값)
# left  = [1]      ← 3 보다 작은 것
# mid   = [3]      ← 3 과 같은 것
# right = [5, 8, 4] ← 3 보다 큰 것

# left 정렬  → [1]
# right 정렬 → pivot = 8
#              left = [5, 4]
#              mid  = [8]
#              right = []
#              left 정렬 → pivot = 4
#                          left  = []
#                          mid   = [4]
#                          right = [5]
#                          → [4, 5]
#              → [4, 5, 8]

# 최종 합치기
# [1] + [3] + [4, 5, 8] = [1, 3, 4, 5, 8]

def quick_sort(nums):
    if len(nums) <= 1:    # 원소 1개 이하면 이미 정렬됨
        return nums

    pivot = nums[len(nums) // 2]   # 중간값을 pivot 으로

    left  = [x for x in nums if x < pivot]   # pivot 보다 작은 것
    mid   = [x for x in nums if x == pivot]  # pivot 과 같은 것
    right = [x for x in nums if x > pivot]   # pivot 보다 큰 것

    # 재귀적으로 정렬 후 합치기
    return quick_sort(left) + mid + quick_sort(right)

print(quick_sort([5, 3, 8, 1, 4]))  # [1, 3, 4, 5, 8]

# -----------------------------
# 시간복잡도: 평균 O(n log n) / 최악 O(n²)
# 공간복잡도: O(log n)
# 특징: 평균적으로 가장 빠름
#------------------------------

# 6. 병합 정렬 (Merge Sort) - 반씩 나누고 합치면서 정렬 하는 방식

# [5, 3, 8, 1, 4] 를 정렬

# 나누기
# [5, 3, 8, 1, 4]
# [5, 3] + [8, 1, 4]
# [5] + [3] + [8] + [1, 4]
# [5] + [3] + [8] + [1] + [4]

# 합치기
# [3, 5] ← [5] + [3] 합치기
# [1, 4] ← [1] + [4] 합치기
# [1, 4, 8] ← [8] + [1, 4] 합치기
# [1, 3, 4, 5, 8] ← [3, 5] + [1, 4, 8] 합치기

def merge_sort(nums):
    if len(nums) <= 1:    # 원소 1개 이하면 이미 정렬됨
        return nums

    mid   = len(nums) // 2
    left  = merge_sort(nums[:mid])   # 왼쪽 절반 재귀 정렬
    right = merge_sort(nums[mid:])   # 오른쪽 절반 재귀 정렬

    return merge(left, right)        # 정렬된 두 리스트 합치기

def merge(left, right):
    result = []
    i = j = 0

    # 두 리스트를 앞에서부터 비교하면서 작은 것부터 추가
    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    # 남은 것 전부 추가
    return result + left[i:] + right[j:]

print(merge_sort([5, 3, 8, 1, 4]))   # [1, 3, 4, 5, 8]

# -----------------------------
# 시간복잡도: O(n log n) 항상 보장
# 공간복잡도: O(n)
# 특징: 안정적 — 항상 O(n log n)
#------------------------------


