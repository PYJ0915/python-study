# 해시 - 임의의 데이터를 고정된 크기의 숫자로 변환하는 함수
# => 빠르게 찾기 위해 이름을 숫자로 바꾸는 기술

# 1. 해시란?
# 파이썬 내장 해시함수
print(hash("apple"))  # 775361519350491479 (임의의 정수값 반환)
print(hash("banana")) # 1669599319471429866
print(hash("apple"))  # 775361519350491479 (같은 값)

# 주요 특징
# 1) 같은 입력 → 항상 같은 출력
# 2) 다른 입력 → 대부분 다른 출력
# 3) 출력으로 입력을 역산할 수 없음

# 2. 해시 테이블 - 해시값을 인덱스로 사용하는 배열
# "apple" → hash() → 3  → 배열[3] 에 저장
# "banana" → hash() → 7 → 배열[7] 에 저장
# "kiwi" → hash() → 1  → 배열[1] 에 저장

# 찾을 때
# "apple" 찾기 → hash("apple") → 3 → 배열[3] 바로 접근 → O(1)

# 그림으로 보면

# 인덱스  값
#   0    None
#   1    "kiwi"
#   2    None
#   3    "apple"
#   4    None
#   ...
#   7    "banana"

# 파이썬 딕셔너리 = 해시 테이블
d = {"apple": 1, "banana": 2, "kiwi": 3}
# 내부적으로
# hash("apple")  → 인덱스 계산 → 값 저장
# hash("banana") → 인덱스 계산 → 값 저장

# 조회
print(d["apple"]) # 1 (hash("apple") → 인덱스 → 바로 접근 O(1))

# 3. 리스트 vs 딕셔너리 속도 차이
import time

# 100만개 데이터
data_list = list(range(1000000))
data_dict = {i: True for i in range(1000000)}

# 리스트에서 찾기
start = time.time()
999999 in data_list # 처음부터 끝까지 탐색
print(time.time() - start) # 0.0030965805053710938

# 딕셔너리에서 찾기
start = time.time()
999999 in data_dict # 해시값으로 바로 접근 O(1)
print(time.time() - start) # 0.0

# 4. 해시 충돌

# 다른 입력인데 같은 해시값이 나오는 경우예요.
# ```
# hash("abc") → 5
# hash("xyz") → 5   ← 충돌!

# 배열[5] 에 둘 다 저장하려면?
# → 같은 인덱스에 연결 리스트로 이어서 저장
# → 최악의 경우 O(n) 이 될 수 있음
# → 파이썬은 내부적으로 충돌 최소화 처리

# 5. 해시 가능 / 불가능
# 해시 가능 — 변경 불가능한 객체
hash(1)           # ✅ int
hash(3.14)        # ✅ float
hash("hello")     # ✅ str
hash((1, 2, 3))   # ✅ tuple

# 해시 불가능 — 변경 가능한 객체
hash([1, 2, 3])   # ❌ list
hash({1: 2})      # ❌ dict
hash({1, 2, 3})   # ❌ set

# 딕셔너리 키로 쓸 수 있는 것 = 해시 가능한 것
d = {}
d["hello"] = 1    # ✅ str
d[(1, 2)] = 2     # ✅ tuple
d[[1, 2]] = 3     # ❌ list → TypeError

# 실생활 비유
# 해시 = 도서관 책 분류 시스템

# 책 제목 → 분류 번호 계산 → 해당 번호 책장에 바로 이동
# "파이썬" → 001.23 → 001번 책장 바로 이동 O(1)

# 리스트 = 분류 없이 순서대로 꽂힌 책장
# "파이썬" 찾으려면 처음부터 끝까지 뒤져야 함 O(n)

# 6. 정리
# 해시 함수  → 데이터를 숫자(인덱스)로 변환
# 해시 테이블 → 해시값을 인덱스로 쓰는 배열
# 딕셔너리   → 파이썬의 해시 테이블 구현체
# 핵심 장점  → 조회 / 삽입 / 삭제 모두 O(1)
# 핵심 단점  → 순서 보장 X (파이썬 3.7+ 은 삽입 순서 보장)
#              해시 충돌 발생 가능
#              메모리를 리스트보다 많이 씀
